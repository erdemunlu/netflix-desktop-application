    public void dinamikAra(String ara){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(model);
        FilmTablosu_Alani.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(ara));
    }

    public void filmleriGetir(){
        model.setRowCount(0); //calısan tablosu her seferinde sıfırlanmış olur.
        ArrayList<Filmler> filmler = new ArrayList<Filmler>();
        filmler = methodlar.filmleriGetir();
        
        if(filmler != null){
           for(Filmler film : filmler){
               Object[] FilmEkle = {film.getProgramAdi(),film.getTurAdi()};
               model.addRow(FilmEkle);
               
           }
       }
        
    }

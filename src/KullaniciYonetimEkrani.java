    private void FilmIzleButonu_AlaniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FilmIzleButonu_AlaniActionPerformed
            String izlenecekFilm = SectiginizFilm_Alani.getText();
            kullanici_ID = methodlar.kullaniciBilgileriTut(KullaniciEmaili);
            Program_ID = methodlar.programIdBul(izlenecekFilm);
            FilmIzleButonu_Alani.setEnabled(false);///Butona tekrar tiklanmasi engellendi.
            if(!puan_Alani.getText().equals("")){
                puan = Integer.parseInt(puan_Alani.getText());
            }
            
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd",Locale.ENGLISH);
            Tarih = sdf.format(jDateChooser1.getDate());
            String ProgramTipi = methodlar.programTipBul(izlenecekFilm);
            int izlenensure =methodlar.izlemeSuresiBul(kullanici_ID, Program_ID);
            ///izlemeSuresi_alani.setText(String.valueOf(izlenensure));
            izlemeSuresi = izlenensure;

            if(ProgramTipi.equals("Film")){
                kaldigiBolum = 1;
            }
            else{
                kaldigiBolum=methodlar.kalinanBolumuBul(kullanici_ID, Program_ID);
                if(kaldigiBolum == 0){
                    kaldigiBolum = 1;
                }
            }
            
            
            if(izlemeSuresi>0 || kaldigiBolum>1){
                methodlar.eskiyiSil(kullanici_ID, Program_ID, izlemeSuresi);
            }
            
            
           ProgramUzunlugu = methodlar.programUzunluguBul(Program_ID);
           BolumUzunlugu_Alani.setText(String.valueOf(ProgramUzunlugu));
           ToplambolumSayisi = methodlar.toplamBolumSayisiniBul(Program_ID);
           ToplamBolumSayisi_Alani.setText(String.valueOf(ToplambolumSayisi));
           
           ///izlemeSuresi_alani.setText(String.valueOf(ToplambolumSayisi));
           
            if(ProgramTipi.equals("Film")){
                bolum_Alani.setText(String.valueOf(kaldigiBolum));
         
            Timer timer = new Timer(50,null);
            timer.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    if(izlemeSuresi<ProgramUzunlugu){
                    izlemeSuresi++;
                    izlemeSuresi_alani.setText(String.valueOf(izlemeSuresi));
                    if(izlemeSuresi == ProgramUzunlugu){
                        UIManager.put("OptionPane.okButtonText", "Tamam.");
                        JOptionPane.showMessageDialog(KullaniciYonetimEkrani.this,"Film Bitti.");
                        ///setVisible(false);
                        timer.stop();
                    }
                    }
                                       
                }
            });
            timer.start();
            
            } 
            else{
                Timer timer = new Timer(50,null);
                timer.addActionListener(new ActionListener() {

                @Override
                public void actionPerformed(ActionEvent e) {
                    
                   if(kaldigiBolum<=ToplambolumSayisi){
                    izlemeSuresi++;
                    bolum_Alani.setText(String.valueOf(kaldigiBolum));
                    izlemeSuresi_alani.setText(String.valueOf(izlemeSuresi));
                    if((izlemeSuresi == ProgramUzunlugu) && (kaldigiBolum<ToplambolumSayisi)){
                        izlemeSuresi =0;
                        kaldigiBolum++;
                        
                        
                    }
                    


                   }
                   
                   if((izlemeSuresi == ProgramUzunlugu) && (kaldigiBolum == ToplambolumSayisi)){
                  UIManager.put("OptionPane.okButtonText", "Tamam.");    
                  JOptionPane.showMessageDialog(KullaniciYonetimEkrani.this,"Dizi Bitti.");
                  //setVisible(false);
                  timer.stop();
                   }
                }
                
            });
            timer.start();
            }
         
            ///burasi.setText(String.valueOf(kullanici_ID) );
            
         
        
           
    }//GEN-LAST:event_FilmIzleButonu_AlaniActionPerformed

